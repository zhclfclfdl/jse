package cmm02.op;

public class No02_2HangDemo {
	public static void main(String[] args) {
		int x = 20, y = 7;
		int add, sub, gob, mok, nmg = 0;
		
		add = x + y;
		sub = x - y;
		gob = x * y;
		mok = x / y;
		nmg = x % y;
		
		System.out.println( "add 의 값  : " +add);
		System.out.println( "sub 의 값  : " +sub);
		System.out.println( "gob 의 값  : " +gob);
		System.out.println( "mok 의 값  : " +mok);
		System.out.println( "nmg 의 값  : " +nmg);
		
	}
}
